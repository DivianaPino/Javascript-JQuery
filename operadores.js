'use strict';

var numero=1;
var numero2=5;

// OPERADORES BASICOS

//suma
var resultadosuma=numero+numero2;
//resta
var resultadoresta=numero-numero2;
//resta
var resultadodivision=numero/numero2;
//Multiplicacion
var resultadomultiplicacion=numero* numero2;

console.log("El resultado de la suma es:" + resultadosuma); //concatenamos con signo +
console.log("El resultado de la resta es:" + resultadoresta);
console.log("El resultado de la division es:" + resultadodivision);
console.log("El resultado de la multiplicacion es:" + resultadomultiplicacion);

//OPERADORES DE INCREMENTO

resultadosuma ++;
console.log("Incremento ++" + resultadosuma);

resultadosuma --;
console.log("Incremento --" + resultadosuma);

